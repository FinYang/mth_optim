%
%--------------------------------------------------------------------------
% This function is projecting a point onto a ball.
%--------------------------------------------------------------------------
%
%--INPUTS------------------------------------------------------------------
%
% y          -     point
% z          -     centre of ball
% R          -     radius of ball
%
%--OUTPUT------------------------------------------------------------------
%
% x          -     projection of y to B_R(z)
%
%--------------------------------------------------------------------------

%--------------------------------------------------------------------------
% DO NOT MODIFY THE FUNCTION DEFINITION!
%--------------------------------------------------------------------------

function x=myProjectBall(y,z,R)

%--------------------------------------------------------------------------
% WRITE YOUR CODE BELOW THIS LINE!
%--------------------------------------------------------------------------

if(norm(y-z)<=R)
    x=y;
else
    x=z+ R/(norm(y-z))*(y-z);
end

end